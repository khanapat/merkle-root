async function main() {
    const client = createPublicClient({
        chain: sepolia,
        transport: http(),
    });

    // hre
    // const client = await viem.getPublicClient();

    // contract instances
    const contract = getContract({
        address: "0xCDC7342576D864D408207a41e261bfc8c953A461",
        abi: stockABI,
        publicClient: client,
    });

    const stock = await contract.read.limitStock() as bigint;
    console.log(`Limit Stock: ${formatEther(stock)} ETH`);

    // read contract
    const limitStock = await client.readContract({
        address: "0xCDC7342576D864D408207a41e261bfc8c953A461",
        abi: stockABI,
        functionName: "limitStock",
        args: [],
    }) as bigint;
    console.log(`Limit Stock: ${formatEther(limitStock)} ETH`);

    const balance = await client.readContract({
        address: "0xA04DdBEaEDE6D85d0a7D49aFed1db6EdD1d8cFf2",
        abi: tokenABI,
        functionName: "balanceOf",
        args: ["0xc083EB69aa7215f4AFa7a22dcbfCC1a33999371C"],
    }) as bigint;
    console.log(`MyToken Balance: ${formatEther(balance)} ETH`);

    // write contract
    const account = privateKeyToAccount(`0x${process.env.PRIVATE_KEY}`)

    const wallet = createWalletClient({
        account,
        chain: sepolia,
        transport: http(),
    })

    const simulate = await client.simulateContract({
        account,
        address: "0xCDC7342576D864D408207a41e261bfc8c953A461",
        abi: stockABI,
        functionName: "setStock",
        args: ["book", 100],
    });
    console.log("Simulation Result:", simulate);

    const hash = await wallet.writeContract(simulate.request);
    console.log(`Transaction Hash: ${hash}`);
}

main().catch((error) => {
    console.error(error);
    process.exitCode = 1;
});
